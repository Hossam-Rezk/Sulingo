def compute_velocity_acceleration(landmarks_sequence):
    """
    حساب السرعة والتسارع
    Input: (30, 63)
    Output: velocity (30, 63), acceleration (30, 63)
    """

    # Velocity = الفرق بين frames
    velocity = np.diff(landmarks_sequence, axis=0)  # (29, 63)
    velocity = np.vstack([velocity, velocity[-1]])  # pad to (30, 63)

    # Acceleration = الفرق بين velocities
    acceleration = np.diff(velocity, axis=0)  # (29, 63)
    acceleration = np.vstack([acceleration, acceleration[-1]])  # pad to (30, 63)

    return velocity, acceleration

