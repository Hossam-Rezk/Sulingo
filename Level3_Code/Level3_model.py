def base64_to_frame(img_data):
    img_bytes = b64decode(img_data.split(',')[1])
    img = Image.open(io.BytesIO(img_bytes))
    img = np.array(img)

    if len(img.shape) == 3:
        img = cv2.cvtColor(img, cv2.COLOR_RGB2BGR)

    return img

