def extract_landmarks_from_frame(frame, hands_detector):
    """استخراج landmarks من frame واحد مع normalization"""

    # تحويل لـ RGB
    rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)

    # معالجة
    result = hands_detector.process(rgb)

    # لو مفيش يد
    if not result.multi_hand_landmarks:
        return np.zeros(63)

    # استخراج أول يد
    hand = result.multi_hand_landmarks[0]
    coords = np.array([[lm.x, lm.y, lm.z] for lm in hand.landmark])  # (21, 3)

    # Normalization
    coords = coords.reshape(21, 3)
    wrist = coords[0]  # المعصم
    coords = coords - wrist  # translate to origin

    # Scale بناءً على حجم الكف
    palm_size = np.linalg.norm(coords[9] - coords[0]) + 1e-6
    coords = coords / palm_size

    return coords.flatten()  # (63,)

def extract_landmarks_from_video(video_path, num_frames=30):
    """
    استخراج landmarks من فيديو كامل
    Returns: (num_frames, 63) أو None لو فشل
    """
    cap = cv2.VideoCapture(video_path)

    if not cap.isOpened():
        return None

    landmarks_sequence = []
    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    if total_frames == 0:
        cap.release()
        return None

    # نأخذ frames بالتساوي
    indices = np.linspace(0, total_frames-1, num_frames, dtype=int)

    for idx in indices:
        cap.set(cv2.CAP_PROP_POS_FRAMES, idx)
        ret, frame = cap.read()

        if ret:
            landmarks = extract_landmarks_from_frame(frame, hands)
            landmarks_sequence.append(landmarks)
        else:
            landmarks_sequence.append(np.zeros(63))

    cap.release()

    # تأكد إن عندنا num_frames بالظبط
    while len(landmarks_sequence) < num_frames:
        landmarks_sequence.append(np.zeros(63))

    return np.array(landmarks_sequence[:num_frames])

