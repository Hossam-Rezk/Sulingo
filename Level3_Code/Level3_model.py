feature_extractor_code = '''
import mediapipe as mp
import cv2
import numpy as np
import pickle

class SignLanguageFeatureExtractor:
    """Feature Extractor for Sign Language Recognition"""

    def __init__(self, num_frames=30):
        self.num_frames = num_frames
        self.mp_hands = mp.solutions.hands
        self.hands = self.mp_hands.Hands(
            static_image_mode=False,
            max_num_hands=2,
            min_detection_confidence=0.3,
            min_tracking_confidence=0.3
        )

        # Load model
        try:
            with open('sign_language_rf_augmented.pkl', 'rb') as f:
                self.model = pickle.load(f)

            with open('word_mapping.pkl', 'rb') as f:
                self.word_to_idx = pickle.load(f)

            self.idx_to_word = {v: k for k, v in self.word_to_idx.items()}
            print(f"✓ Model loaded: {len(self.word_to_idx)} words")
        except:
            self.model = None
            print("⚠ Model not found")

    def extract_from_frame(self, frame):
        """Extract landmarks from single frame"""
        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        result = self.hands.process(rgb)

        if not result.multi_hand_landmarks:
            return None

        hand = result.multi_hand_landmarks[0]
        coords = np.array([[lm.x, lm.y, lm.z] for lm in hand.landmark])

        # Normalization
        coords = coords.reshape(21, 3)
        wrist = coords[0]
        coords = coords - wrist

        palm_size = np.linalg.norm(coords[9] - coords[0]) + 1e-6
        coords = coords / palm_size

        return coords.flatten()

    def extract_from_video(self, video_path):
        """Extract landmarks from video file"""
        cap = cv2.VideoCapture(video_path)

        if not cap.isOpened():
            return None

        total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
        if total_frames == 0:
            cap.release()
            return None

        indices = np.linspace(0, total_frames-1, self.num_frames, dtype=int)
        valid_landmarks = []

        for idx in indices:
            cap.set(cv2.CAP_PROP_POS_FRAMES, idx)
            ret, frame = cap.read()

            if ret:
                landmarks = self.extract_from_frame(frame)
                if landmarks is not None:
                    valid_landmarks.append(landmarks)

        cap.release()

        if len(valid_landmarks) < self.num_frames // 2:
            return None

        while len(valid_landmarks) < self.num_frames:
            valid_landmarks.append(valid_landmarks[-1])

        return np.array(valid_landmarks[:self.num_frames])

    def predict(self, video_path):
        """Extract and predict"""
        if self.model is None:
            return None, 0.0

        features = self.extract_from_video(video_path)

        if features is None:
            return None, 0.0

        features_flat = features.reshape(1, -1)

        prediction = self.model.predict(features_flat)[0]
        probabilities = self.model.predict_proba(features_flat)[0]

        predicted_word = self.idx_to_word[prediction]
        confidence = probabilities[prediction]

        return predicted_word, confidence

    def predict_from_frames(self, frames_list):
        """Predict from list of frames"""
        if self.model is None or len(frames_list) < self.num_frames:
            return None, 0.0

        landmarks_sequence = []

        for frame in frames_list[-self.num_frames:]:
            landmarks = self.extract_from_frame(frame)
            if landmarks is not None:
                landmarks_sequence.append(landmarks)

        if len(landmarks_sequence) < self.num_frames // 2:
            return None, 0.0

        while len(landmarks_sequence) < self.num_frames:
            landmarks_sequence.append(landmarks_sequence[-1])

        features = np.array(landmarks_sequence[:self.num_frames])
        features_flat = features.reshape(1, -1)

        prediction = self.model.predict(features_flat)[0]
        probabilities = self.model.predict_proba(features_flat)[0]

        predicted_word = self.idx_to_word[prediction]
        confidence = probabilities[prediction]

        return predicted_word, confidence

    def __del__(self):
        if hasattr(self, 'hands'):
            self.hands.close()
'''

