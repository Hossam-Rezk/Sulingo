def augment_landmarks(landmarks_seq):
    """
    Data augmentation للـ landmarks
    Input: (30, 63)
    Output: (30, 63) augmented
    """
    augmented = landmarks_seq.copy()

    # 1. Horizontal Flip (قلب أفقي - زي المرايا)
    if np.random.rand() > 0.5:
        augmented = augmented.reshape(30, 21, 3)
        augmented[:, :, 0] = -augmented[:, :, 0]  # flip x-coordinate
        augmented = augmented.reshape(30, 63)

    # 2. Gaussian Noise (ضوضاء)
    if np.random.rand() > 0.5:
        noise = np.random.randn(*augmented.shape) * 0.01
        augmented = augmented + noise

    # 3. Scale Jitter (تغيير الحجم شوية)
    if np.random.rand() > 0.5:
        scale = np.random.uniform(0.9, 1.1)
        augmented = augmented * scale

    # 4. Time Warping (تمطيط/ضغط الزمن)
    if np.random.rand() > 0.5:
        # اختار frames عشوائية (مع تكرار)
        indices = np.sort(np.random.choice(30, size=30, replace=True))
        augmented = augmented[indices]

    # 5. Rotation (دوران بسيط)
    if np.random.rand() > 0.5:
        angle = np.random.uniform(-5, 5) * np.pi / 180  # ±5 degrees
        augmented = augmented.reshape(30, 21, 3)

        # Rotate around z-axis
        cos_a = np.cos(angle)
        sin_a = np.sin(angle)

        x = augmented[:, :, 0]
        y = augmented[:, :, 1]

        augmented[:, :, 0] = x * cos_a - y * sin_a
        augmented[:, :, 1] = x * sin_a + y * cos_a

        augmented = augmented.reshape(30, 63)

    return augmented

