import mediapipe as mp
import cv2
import numpy as np

class MSASLFeatureExtractor:
    """
    Feature Extractor لـ MS-ASL Dataset
    يستخدمه حسام للتدريب والـ API
    """

    def __init__(self, num_frames=30):
        self.num_frames = num_frames
        self.mp_hands = mp.solutions.hands
        self.hands = self.mp_hands.Hands(
            static_image_mode=False,
            max_num_hands=2,
            min_detection_confidence=0.5,
            min_tracking_confidence=0.5
        )

    def extract_from_video(self, video_path):
        """
        استخراج landmarks من فيديو
        Returns: (num_frames, 63) أو None
        """
        cap = cv2.VideoCapture(video_path)

        if not cap.isOpened():
            return None

        landmarks_sequence = []
        total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

        if total_frames == 0:
            cap.release()
            return None

        # Sample frames uniformly
        indices = np.linspace(0, total_frames-1, self.num_frames, dtype=int)

        for idx in indices:
            cap.set(cv2.CAP_PROP_POS_FRAMES, idx)
            ret, frame = cap.read()

            if ret:
                landmarks = self._extract_from_frame(frame)
                landmarks_sequence.append(landmarks)
            else:
                landmarks_sequence.append(np.zeros(63))

        cap.release()

        # Padding
        while len(landmarks_sequence) < self.num_frames:
            landmarks_sequence.append(np.zeros(63))

        result = np.array(landmarks_sequence[:self.num_frames])

        # Check if hand was detected
        if result.max() == 0:
            return None

        return result

    def _extract_from_frame(self, frame):
        """استخراج من frame واحد مع normalization"""
        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        result = self.hands.process(rgb)

        if not result.multi_hand_landmarks:
            return np.zeros(63)

        hand = result.multi_hand_landmarks[0]
        coords = np.array([[lm.x, lm.y, lm.z] for lm in hand.landmark])

        # Signer Normalization
        coords = coords.reshape(21, 3)
        wrist = coords[0]
        coords = coords - wrist

        palm_size = np.linalg.norm(coords[9] - coords[0]) + 1e-6
        coords = coords / palm_size

        return coords.flatten()

    def compute_motion_features(self, landmarks_sequence):
        """حساب velocity & acceleration"""
        velocity = np.diff(landmarks_sequence, axis=0)
        velocity = np.vstack([velocity, velocity[-1]])

        acceleration = np.diff(velocity, axis=0)
        acceleration = np.vstack([acceleration, acceleration[-1]])

        return velocity, acceleration

    def extract_full_features(self, video_path):
        """
        استخراج landmarks + motion features
        Returns: (num_frames, 189) - positions + velocity + acceleration
        """
        landmarks = self.extract_from_video(video_path)

        if landmarks is None:
            return None

        velocity, acceleration = self.compute_motion_features(landmarks)

        # Combine all features
        full_features = np.concatenate([landmarks, velocity, acceleration], axis=1)

        return full_features

    def __del__(self):
        if hasattr(self, 'hands'):
            self.hands.close()


